
public class Example {
	
	private String name;
	private int age;
	private String address;
	 
	//Constructor1
	public Example() {
		//Default
	}
	
	//Constructor2
	public Example(String name, int age) {
		this.name=name;
		this.age=age;
	}
	
	//Constructor3
	public Example(String name, int age, String address) {
		this.name=name;
		this.age=age;
		this.address=address;
	}
}
